package org.bpy.score.club.ui.outline;


import org.bpy.score.club.club.Club;
import org.bpy.score.club.club.ClubDescription;
import org.bpy.score.club.club.Member;
import org.bpy.score.club.club.Members;
import org.bpy.score.club.club.Official;
import org.bpy.score.club.club.Officials;
import org.bpy.score.club.club.Player;
import org.bpy.score.club.club.ScoreKeeper;
import org.bpy.score.club.club.Team;
import org.bpy.score.club.club.Umpire;
import org.bpy.score.internationalization.club.Messages;
import org.eclipse.emf.ecore.EObject;
import org.eclipse.osgi.util.NLS;
import org.eclipse.xtext.ui.editor.outline.IOutlineNode;
import org.eclipse.xtext.ui.editor.outline.impl.DefaultOutlineTreeProvider;

/**
 * Customization of the default outline structure.
 *
 * See https://www.eclipse.org/Xtext/documentation/310_eclipse_support.html#outline
 */
public class ClubOutlineTreeProvider extends DefaultOutlineTreeProvider {
	
	@Override
	public void _createChildren(IOutlineNode parentNode, EObject parentModelElement) {
		
		if (parentModelElement instanceof Club) {
			
			ClubDescription clubDescription = ((Club)parentModelElement).getClubDescription();

			createNode(parentNode, clubDescription.getMembers());
			for (Team team : clubDescription.getTeams()) {
				createNode(parentNode, team);
			}
			createNode(parentNode, clubDescription.getOfficials());
	
		} else if (parentModelElement instanceof Members) {
			for (Member member : ((Members)parentModelElement).getMembers() ) {
				createNode(parentNode, member);
			}

		} else if (parentModelElement instanceof Team) {
			for (Player player : ((Team)parentModelElement).getPlayers() ) {
				createNode(parentNode, player);
			}
			
		} else if (parentModelElement instanceof Officials) {
			for (Official official : ((Officials)parentModelElement).getOfficials()) {
				createNode(parentNode, official);
			}
		}
	}
	
	/**
	 * Set name for the members category in the outline view
	 * 
	 * @param members reference on the members rule
	 * 
	 * @return name for the members category in the outline view
	 */
	@SuppressWarnings("java:S100")
	public Object _text(Members members) {
	    return Messages.ClubOutlineTreeProvider_Members;
	}
	
	/**
	 * Set name for the officials category in the outline view
	 * 
	 * @param officials reference on the officials rule
	 * 
	 * @return name for the officials category in the outline view
	 */
	@SuppressWarnings("java:S100")
	public Object _text(Officials officials) {
	    return Messages.ClubOutlineTreeProvider_Officials;
	}
	
	/**
	 * Set name for the officials category in the outline view
	 * 
	 * @param player reference on the Player rule
	 * 
	 * @return name for the officials category in the outline view
	 */
	@SuppressWarnings("java:S100")
	public Object _text(Player player) {
	    return player.getPlayerName().getName();
	}

	/**
	 * Set name for the officials category in the outline view
	 * 
	 * @param official Official container
	 * 
	 * @return name for the officials category in the outline view
	 */
	@SuppressWarnings("java:S100")
	public Object _text(Official official) {
		if (official instanceof ScoreKeeper) {
			return NLS.bind(Messages.ClubOutlineTreeProvider_ScooreKeeper, ((ScoreKeeper)official).getName().getName() );  
		} else {
			return NLS.bind(Messages.ClubOutlineTreeProvider_Umpire, ((Umpire)official).getName().getName() );  
		}
	}
}
